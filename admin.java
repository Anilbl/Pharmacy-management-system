    public void add_fill_table() {
    try {
        Connection Con2 = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy", "root", "Mych0ice@12345");
        DefaultTableModel model1 = (DefaultTableModel) users_tbl.getModel();
            
            model1.setRowCount(0);    
            Statement st=Con2.createStatement();
            ResultSet rs5 = st.executeQuery("SELECT * FROM login");
            ResultSetMetaData rsmd=(ResultSetMetaData) rs5.getMetaData();
            DefaultTableModel model =(DefaultTableModel) users_tbl.getModel();
            
            int cols = rsmd.getColumnCount();
            String[] colName = new String[cols];
            for(int i=0;    i<cols; i++){
                colName[i]=rsmd.getColumnName(i+1);
                model.setColumnIdentifiers(colName);
                String id,user_name,password,post,email;
                while(rs5.next()){
                    id = rs5.getString(1);
                    user_name = rs5.getString(2);
                    password = rs5.getString(3);
                    post = rs5.getString(4);
                    email = rs5.getString(5);

                    String[] row = {id,user_name,password,post,email};
                    model.addRow(row);
                }
                
            }

        st.close();
        Con2.close();

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage());
    }
}

