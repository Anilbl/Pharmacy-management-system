    private void search_userActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_userActionPerformed
        // TODO add your handling code here:
        try {
            Connection Con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy", "root", "Mych0ice@12345");
            String sql = "SELECT * FROM login WHERE sno = ?";
    
            // Create a PreparedStatement
            try (PreparedStatement statement = Con.prepareStatement(sql)) {
        
        
                
                int Id = Integer.parseInt(id.getText());
                statement.setInt(1, Id);
        
                // Execute the query
                ResultSet resultSet = statement.executeQuery();
        
                // Check if any results were returned
                if (resultSet.next()) {
                    // Set the values in your text fields based on the retrieved data
                    username_txtfield.setText(resultSet.getString("username"));
                    password_txtfield.setText(resultSet.getString("password"));
                    post_txtfield.setText(resultSet.getString("post"));
                    email_txtfield.setText(resultSet.getString("email"));
                    
                    
                    
            
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "No data found for the specified med_id.");
                    clearinputs();
                }
            }
    
            // Close the connection
            Con.close();
        }
        catch (SQLException | NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
        finally
        {
            menu.DatabaseManager.closeConnection();
        }
        
    }//GEN-LAST:event_search_userActionPerformed

